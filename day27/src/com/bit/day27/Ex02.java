package com.bit.day27;

import java.net.MalformedURLException;
import java.net.URL;

public class Ex02 {

	public static void main(String[] args) {
		// url
		// scheme:[//[user:password@]host[:port]][/]path[?query][#fragment]
		// 프로토콜://도메인:포트번호/패스?쿼리#지시자
		// 지시자 - 상단으로(top) 버튼 등
		// https://search.naver.com/search.naver?query=web 웹 검색
		
		String spec = "https://www.seoul.go.kr/news/news_tender.do#view/318319"; // https secret보안프로토콜 http 일반
		java.net.URL url = null;
		
		try {
			url = new URL(spec);		
			System.out.println("프로토콜 : " + url.getProtocol()); // http
			System.out.println("도메인(ip) : " + url.getHost()); // www.seoul.go.kr
			// http - default port : 80;
			// https - default port : 443;
			System.out.println("포트번호 : " + url.getPort()); // -1
			System.out.println("포트번호(default) : " + url.getDefaultPort()); // 443
			System.out.println("패스경로 : " + url.getPath()); // /news/news_tender.do
			System.out.println("url쿼리 : " + url.getQuery()); // null // 정보전달 쿼리
		} catch(MalformedURLException e) {
			e.printStackTrace();
		}
	}
}
